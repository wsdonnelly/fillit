#include "libft.h"

char	*ft_strdup(const char *src)
{
	int		len;
	int		i;
	char	*dup;

	len = 0;
	i = 0;
	len = ft_strlen(src);
	dup = (char *)malloc(sizeof(char) * len + 1);
	if (dup == NULL)
		return (0);
	i = 0;
	while (i < len)
	{
		dup[i] = src[i];
		i++;
	}
	dup[i] = '\0';
	return (dup);
}
